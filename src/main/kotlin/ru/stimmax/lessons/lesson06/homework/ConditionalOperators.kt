// Задание 6: "Конвертация температуры"

// Создайте функцию, которая конвертирует температуру из градусов Цельсия в Фаренгейты и наоборот в зависимости от указанной
// единицы измерения (C/F). Единицу измерения нужно передать вторым аргументом функции. Несколько аргументов передаются через
// запятую. Распечатай в консоль результат конвертации с добавлением единицы измерения. Чтобы добавить единицу измерения после
// результата используй функцию печати без переноса строки print("C") или print("F").

fun degree (temperature: Int, measure: String) {
    if (measure == "F") {
        val temp = (temperature - 32)/1.8
        print(temp.toString() + "F")
    } else if (measure == "C"){
        val temp = temperature * 1.8 + 32
        println(temp.toString() + "F")
    }
}

// Задание 7: "Подбор одежды по погоде"

// Напишите функцию, которая на основе температуры воздуха рекомендует тип одежды: "куртка и шапка" при температуре ниже +10,
